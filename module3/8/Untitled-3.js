// Declare and evaluate expressions with different operators
let result1 = 10 + 5 * 2;      // Multiplication happens before addition
let result2 = (10 + 5) * 2;    // Parentheses change the order
let result3 = 100 / 10 + 5 - 3 * 2; // Division, multiplication, then addition/subtraction

// Display results
console.log("result1 = 10 + 5 * 2 =", result1);      // Expected: 20
console.log("result2 = (10 + 5) * 2 =", result2);    // Expected: 30
console.log("result3 = 100 / 10 + 5 - 3 * 2 =", result3); // Expected: 10 + 5 - 6 = 9

