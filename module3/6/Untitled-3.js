// Simulated primitive data types in JavaScript

// Integer
let intVal = 42; // JavaScript only has "number", but this is an integer

// Float
let floatVal = 3.14; // Still a number (floating point)

// Double (same as float in JavaScript, but shows precision)
let doubleVal = 123.45678901234567; // All numbers are 64-bit floats

// Character (single-character string)
let charVal = 'A';

// Boolean
let boolVal = true;

// Displaying values in the console
console.log("Integer value (int):", intVal);
console.log("Floating point value (float):", floatVal);
console.log("Double precision value (double):", doubleVal);
console.log("Character value (char):", charVal);
console.log("Boolean value (boolean):", boolVal);
